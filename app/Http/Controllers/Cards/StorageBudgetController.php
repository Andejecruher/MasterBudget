    {
        $storage = new StorageBudget();
        $storage->storage = $request->storage;
        $storage->percentageStorage  = $request->percentageStore;
        $storage->budget_id = $request->budget_id;
        $storage->company_id = $request->company_id;
        $storage->save();
    }

